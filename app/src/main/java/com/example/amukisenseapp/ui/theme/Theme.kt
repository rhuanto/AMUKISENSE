@Composable
fun AMUKISENSEAppTheme(
    darkTheme: Boolean = false, // Siempre usar tema claro
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    // Siempre usar el esquema de colores claro
    val colorScheme = LightColorScheme
    
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // Barra de estado transparente/sin color
            window.statusBarColor = Color.Transparent.toArgb()
            // Iconos oscuros en la barra de estado (para fondo claro)
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = true
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
